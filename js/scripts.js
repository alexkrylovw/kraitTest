///price-block-1
//in
$("#price-block-1").mouseover(function(){
    $("#price-block-1").css("background", "#fff");
    $("#price-block-1").css("-webkit-box-shadow", "0px 3px 27px -4px rgba(0,0,0,0.75)");
    $("#price-block-1").css("-moz-box-shadow", "0px 3px 27px -4px rgba(0,0,0,0.75)");
    $("#price-block-1").css("box-shadow", "0px 3px 27px -4px rgba(0,0,0,0.75)");
    $("#price-1").css("color", "#fff");
    $("#price-button-1").css("border-width", "3px");
    $("#price-shape-1-1").css("background", "linear-gradient(to right, #fd712c 0%,#f31e7a 100%)");
    $("#price-shape-2-1").css("background", "linear-gradient(to right, #fd712c 0%,#f31e7a 100%)"); /////???
    $("#price-button-1").css("background", "linear-gradient(to right, #fd712c 0%,#f31e7a 100%)");
    $("#price-button-1").css("color", "#fff");
    $("#price-list-1").css("color","#777");

});
//out
$("#price-block-1").mouseout(function(){
    $("#price-block-1").css("background", "#f7fafc");
    $("#price-block-1").css("-webkit-box-shadow", "none");
    $("#price-block-1").css("-moz-box-shadow", "none");
    $("#price-block-1").css("box-shadow", "none");
    $("#price-1").css("color", "#222");
    $("#price-button-1").css("border-width", "1px");
    $("#price-shape-1-1").css("background", "#fff");
    $("#price-button-1").css("background", "#fff");
    $("#price-button-1").css("color", "#222");
    $("#price-list-1").css("color","#828282");
});
